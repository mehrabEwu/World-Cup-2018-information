    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
  

      ///************new code
          $this->validate($request, [
            'cname' => 'required',
            'tname' => 'required',
            'image' => 'image|nullable',
            'wcwin' => 'required'


        ]);
        // Handle File Upload
        if($request->hasFile('image')){
            // Get filename with the extension
            $filenameWithExt = $request->file('image')->getClientOriginalName();
            // Get just filename
            $filename = pathinfo($filenameWithExt, PATHINFO_FILENAME);
            // Get just ext
            $extension = $request->file('image')->getClientOriginalExtension();
            // Filename to store
            $fileNameToStore= $filename.'_'.time().'.'.$extension;
            // Upload Image
            $path = $request->file('image')->storeAs('public/team', $fileNameToStore);
        } 
        else {
            $fileNameToStore = 'noimage.jpg';
        }
        // Create Post
        $country = new Country;
        $country->cname = $request->input('cname');
        $country->tname = $request->input('tname');
        $country->image = $fileNameToStore;
         $country->wcwin = $request->input('wcwin');
        $country->save();

        return redirect('/adminDashboard/addCountry')->with('status', 'Your team has been uploaded!');


       
    }

